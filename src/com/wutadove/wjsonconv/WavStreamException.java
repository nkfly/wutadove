package com.wutadove.wjsonconv;

public class WavStreamException extends Exception
{
	/**
	 * 
	 */
	private static final long serialVersionUID = -4941704964077025604L;

	public WavStreamException()
	{
		super();
	}

	public WavStreamException(String message)
	{
		super(message);
	}

	public WavStreamException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public WavStreamException(Throwable cause) 
	{
		super(cause);
	}
}
